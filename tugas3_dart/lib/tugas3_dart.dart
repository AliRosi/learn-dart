void loops() {
  // Looping menggunakan for
  for (int i = 0; i <= 5; i++) {
    print('Hello World $i');
  }

  // Looping menggunakan while
  int i = 1;
  while (i <= 5) {
    print('Perulangan ke-$i');
    i++;
  }

  // Looping menggunakan do while
  int j = 1;
  do {
    print('Ulang ke-$j');
    j++;
  } while (j <= 5);

   
}

